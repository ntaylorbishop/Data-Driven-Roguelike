#include "Game/State.hpp"

//---------------------------------------------------------------------------------------------------------------------------
//STRUCTORS
//---------------------------------------------------------------------------------------------------------------------------
State::State() 
	: m_name("")
{ }
State::State(String name) 
	: m_name(name)
{ }

//---------------------------------------------------------------------------------------------------------------------------
//GETTERS SETTERS
//---------------------------------------------------------------------------------------------------------------------------
String State::GetName() const {
	return m_name;
}

////////////////////////////////////////////////////
//------------------------------------------
//---------------------------------------------------------------------------------------------------------------------------
//OPERATOR OVERLOADS
//---------------------------------------------------------------------------------------------------------------------------
bool operator==(const State& lhs, const State& rhs) {
	return lhs.GetName() == rhs.GetName();
}
bool operator!=(const State& lhs, const State& rhs) {
	return !(lhs == rhs);
}